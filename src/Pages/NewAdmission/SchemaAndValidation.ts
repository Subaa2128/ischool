import * as Yup from "yup";

export const validationSchema = Yup.object().shape({
  staff: Yup.object().shape({
    checkbox: Yup.boolean(),
    staffName: Yup.string(),
  }),
  admission: Yup.object().shape({
    applicationNo: Yup.string().required("required"),
    admissionNo: Yup.string().required(" required"),
    emisNo: Yup.string().required("required"),
    applicationReceivedBy: Yup.string().required("required"),
    DateOfAdmission: Yup.string().required("required"),
    AdmissionRequiredFor: Yup.string().required("required"),
    academicYear: Yup.string().required("required"),
    grade: Yup.string().required("required"),
  }),
  student: Yup.object().shape({
    nameInEnglish: Yup.string().required("Required"),
    nameInTamil: Yup.string().required("Required"),
    gender: Yup.string().required("Required"),
    dateOfBirth: Yup.string().required("Required"),
    dataOfBirthInWords: Yup.string().required("Required"),
    aadharCardNo: Yup.string().required("Required"),
    motherTongue: Yup.string().required("Required"),
    nationality: Yup.string().required("Required"),
    caste: Yup.string().required("Required"),
    bloodGroup: Yup.string().required("Required"),
    religion: Yup.string().required("Required"),
    community: Yup.string().required("Required"),
  }),
  parent: Yup.object().shape({
    fatherDetails: Yup.object().shape({
      name: Yup.string().required("Required"),
      dateOfBirth: Yup.string().required("Required"),
      mobileNo: Yup.string().required("Required"),
      emailAddress: Yup.string().required("Required"),
      aadarNo: Yup.string().required("Required"),
      educationQalification: Yup.string().required("Required"),
      occupation: Yup.string().required("Required"),
      annualIncome: Yup.string().required("Required"),
    }),
    motherDetails: Yup.object().shape({
      name: Yup.string().required("Required"),
      dateOfBirth: Yup.string().required("Required"),
      mobileNo: Yup.string().required("Required"),
      emailAddress: Yup.string().required("Required"),
      aadarNo: Yup.string().required("Required"),
      educationQalification: Yup.string().required("Required"),
      occupation: Yup.string().required("Required"),
      annualIncome: Yup.string().required("Required"),
    }),
  }),
  siblings: Yup.array().of(
    Yup.object().shape({
      sibling: Yup.string().required("Required"),
      name: Yup.string().required("Required"),
      class: Yup.string().required("Required"),
      dateOfBirth: Yup.string().required("Required"),
      instituteName: Yup.string().required("Required"),
      academicYear: Yup.string().required("Required"),
    })
  ),
  previousStudy: Yup.object().shape({
    instituteName: Yup.string().required("Required"),
    academicYear: Yup.string().required("Required"),
    class: Yup.string().required("Required"),
    medium: Yup.string().required("Required"),
    matric: Yup.string().required("Required"),
    marks: Yup.string().required("Required"),
  }),
  upload: Yup.object().shape({
    transferCertificate: Yup.string().required("Required"),
    studentPhoto: Yup.string().required("Required"),
    birthCertificate: Yup.string().required("Required"),
    tenthMarksheet: Yup.string().required("Required"),
  }),
});

